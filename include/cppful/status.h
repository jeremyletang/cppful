#ifndef CPPFUL_STATUS
#define CPPFUL_STATUS

#include <string>

namespace cf {

enum class status {
    ok = 200,
    created = 201,
    accepted = 202,
    no_content = 204,
    multiple_choices = 300,
    moved_permanently = 301,
    moved_temporarily = 302,
    not_modified = 304,
    bad_request = 400,
    unauthorized = 401,
    forbidden = 403,
    not_found = 404,
    not_supported = 405,
    not_acceptable = 406,
    internal_server_error = 500,
    not_implemented = 501,
    bad_gateway = 502,
    service_unavailable = 503,
    unknown
};

std::string to_string(cf::status s);
std::ostream &operator << (std::ostream &os, cf::status s);

}

#endif
